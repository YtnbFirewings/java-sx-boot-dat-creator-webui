package org.insanedevelopment.nx.sx.sxbootdatcreatorwebui;

public class FileConversionException extends RuntimeException {

	private static final long serialVersionUID = 1L;

	public FileConversionException() {
		super();
	}

	public FileConversionException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public FileConversionException(String message, Throwable cause) {
		super(message, cause);
	}

	public FileConversionException(String message) {
		super(message);
	}

	public FileConversionException(Throwable cause) {
		super(cause);
	}

}
